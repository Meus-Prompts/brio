const supportedLocales = Object.keys(translations);
const storageKey = "brio-language";

const menuToggle = document.querySelector(".menu-toggle");
const nav = document.querySelector(".nav");
const languageSelect = document.getElementById("language-select");

let currentLocale = "pt-BR";

function getNestedValue(source, path) {
  return path.split(".").reduce((value, key) => {
    if (value === undefined || value === null) {
      return undefined;
    }

    return value[key];
  }, source);
}

function normalizeLocale(locale) {
  if (!locale) {
    return null;
  }

  const normalized = locale.toLowerCase();

  if (normalized.startsWith("pt-br")) {
    return "pt-BR";
  }

  if (normalized.startsWith("pt-pt") || normalized === "pt") {
    return "pt-PT";
  }

  if (normalized.startsWith("pt")) {
    return "pt-BR";
  }

  if (normalized.startsWith("en")) {
    return "en";
  }

  if (normalized.startsWith("es")) {
    return "es";
  }

  return null;
}

function getPreferredLocale() {
  const storedLocale = normalizeLocale(window.localStorage.getItem(storageKey));

  if (storedLocale) {
    return storedLocale;
  }

  const browserLocales = Array.isArray(navigator.languages) && navigator.languages.length
    ? navigator.languages
    : [navigator.language];

  for (const locale of browserLocales) {
    const normalizedLocale = normalizeLocale(locale);

    if (normalizedLocale && supportedLocales.includes(normalizedLocale)) {
      return normalizedLocale;
    }
  }

  return "pt-BR";
}

function updateMenuToggleLabel() {
  if (!menuToggle) {
    return;
  }

  const isOpen = nav ? nav.classList.contains("open") : false;
  const labelKey = isOpen ? "header.menuToggleClose" : "header.menuToggleOpen";
  const label = getNestedValue(translations[currentLocale], labelKey);

  if (label) {
    menuToggle.setAttribute("aria-label", label);
  }
}

function applyTranslations(locale) {
  const dictionary = translations[locale] || translations["pt-BR"];
  currentLocale = locale;

  document.documentElement.lang = locale;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(dictionary, key);

    if (typeof value !== "string") {
      return;
    }

    const attribute = element.dataset.i18nAttr;

    if (attribute) {
      element.setAttribute(attribute, value);
      return;
    }

    element.textContent = value;
  });

  if (languageSelect) {
    languageSelect.value = locale;
  }

  updateMenuToggleLabel();
}

function setLocale(locale) {
  const nextLocale = supportedLocales.includes(locale) ? locale : "pt-BR";
  window.localStorage.setItem(storageKey, nextLocale);
  applyTranslations(nextLocale);
}

if (menuToggle && nav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
    updateMenuToggleLabel();
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      menuToggle.setAttribute("aria-expanded", "false");
      updateMenuToggleLabel();
    });
  });
}

if (languageSelect) {
  languageSelect.addEventListener("change", (event) => {
    setLocale(event.target.value);
  });
}

const yearElement = document.getElementById("year");
if (yearElement) {
  yearElement.textContent = new Date().getFullYear();
}

const revealItems = document.querySelectorAll(".reveal");
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.15 }
);

revealItems.forEach((item) => observer.observe(item));

let vantaEffect;

window.addEventListener("DOMContentLoaded", () => {
  applyTranslations(getPreferredLocale());

  if (window.VANTA && !vantaEffect) {
    vantaEffect = window.VANTA.NET({
      el: "#hero",
      mouseControls: true,
      touchControls: true,
      gyroControls: false,
      minHeight: 200.0,
      minWidth: 200.0,
      scale: 1.0,
      scaleMobile: 1.0,
      backgroundColor: 0x061008,
      color: 0x2f651e,
    });
  }
});

window.addEventListener("beforeunload", () => {
  if (vantaEffect) {
    vantaEffect.destroy();
  }
});
